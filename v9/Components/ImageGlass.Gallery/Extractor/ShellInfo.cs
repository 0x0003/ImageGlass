namespace ImageGlass.Gallery;


/// <summary>
/// Contains shell icons and shell file type.
/// </summary>
public class ShellInfo
{
    /// <summary>
    /// Error.
    /// </summary>
    public Exception Error = null;
    /// <summary>
    /// Mime type.
    /// </summary>
    public string FileType = null;
    /// <summary>
    /// Small shell icon.
    /// </summary>
    public Image SmallIcon = null;
    /// <summary>
    /// Large shell icon.
    /// </summary>
    public Image LargeIcon = null;
}
